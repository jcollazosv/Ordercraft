    private void btn_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registroActionPerformed
        String nombres = txtnombres.getText();
        String apellidos = txtapellidos.getText();
        String email = txtemail.getText();
        String pass = txtpassword.getText();
        String tipousuario = combo.getSelectedItem().toString();

        if (nombres.isEmpty() || apellidos.isEmpty() || email.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "DEBE COMPLETAR TODOS LOS DATOS");
        } else {
            if (tipousuario.equalsIgnoreCase("Seleccionar")) {
                JOptionPane.showMessageDialog(null, "SELECCIONE UN ROL");
            } else {
                try {
                    String consulta = "INSERT INTO login (nombres,apellidos,contrase√±a,rol,email) VALUES ('" + nombres + "','" + apellidos + "','" + pass + "','" + tipousuario + "','" + email + "')";
                    PreparedStatement ps = (PreparedStatement) cn.prepareStatement(consulta);
                    ps.executeUpdate();
                    limpiar();
                    JOptionPane.showMessageDialog(null, "DATOS GUARDADOS CORRECTAMENTE");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "NO SE PUDO GUARDAR USUARIO" + e);

                }
            }

        }
    }//GEN-LAST:event_btn_registroActionPerformed

    void limpiar() {
        txtnombres.setText("");
        txtapellidos.setText("");
        txtemail.setText("");
        txtpassword.setText("");
    }


